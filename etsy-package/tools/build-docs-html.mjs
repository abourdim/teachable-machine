/**
 * Build HTML twins for every .md file in the etsy-package tree.
 *
 * Walks etsy-package/ (excluding TeachableMachine-* and tools/ and output/
 * sub-screenshots), converts each .md to a paired .html alongside it
 * with a shared dark-mode-aware stylesheet. RTL auto-detected for
 * files whose name ends with -ar.md.
 *
 * Requires: pandoc on PATH.
 *
 * Usage:
 *   node etsy-package/tools/build-docs-html.mjs          # all .md
 *   node etsy-package/tools/build-docs-html.mjs --only README.md
 */

import { execFileSync } from 'child_process';
import { readdirSync, statSync, writeFileSync, existsSync } from 'fs';
import { resolve, join, dirname, relative, basename } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const PKG = resolve(__dirname, '..');

const onlyIdx = process.argv.indexOf('--only');
const ONLY = onlyIdx > 0 ? process.argv[onlyIdx + 1] : null;

// Folders inside etsy-package to scan. Intentionally excludes:
//   - tools/  (has its own .js/.mjs docstrings)
//   - TeachableMachine-* (versioned build artifact, not source)
//   - output/  (heavy binary outputs; but we do pick up output/<lang>/README.md)
//   - accessibility/ (gigantic autogenerated text files)
const INCLUDES = [
  '',                       // PKG root (README.md, USERGUIDE.md, MARKETING-GUIDE.md)
  'seller-only',            // all listing / outreach / testimonial files
  'output/en', 'output/fr', 'output/ar',  // per-language READMEs only (first level)
];

function collect() {
  const hits = [];
  for (const rel of INCLUDES) {
    const dir = resolve(PKG, rel);
    if (!existsSync(dir)) continue;
    for (const f of readdirSync(dir)) {
      if (!f.endsWith('.md')) continue;
      if (ONLY && f !== ONLY) continue;
      const abs = join(dir, f);
      if (statSync(abs).isFile()) hits.push(abs);
    }
  }
  return hits;
}

