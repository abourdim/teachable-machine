// Build a sibling-language nav bar for files ending in -en/-fr/-ar.
function siblingBar(fileBase) {
  const match = fileBase.match(/^(.+)-(en|fr|ar)\.md$/i);
  if (!match) return '';
  const [, stem] = match;
  const langs = ['en', 'fr', 'ar'];
  const label = { en: 'EN', fr: 'FR', ar: 'AR' };
  return `<nav class="sibling-bar">
  ${langs.map(l => `<a href="${stem}-${l}.html">${label[l]}</a>`).join('\n  ')}
</nav>`;
}

function lang(fileBase) {
  if (fileBase.endsWith('-ar.md')) return 'ar';
  if (fileBase.endsWith('-fr.md')) return 'fr';
  return 'en';
}

function render(mdPath) {
  const file = basename(mdPath);
  const htmlPath = mdPath.replace(/\.md$/, '.html');
  const body = execFileSync('pandoc', [mdPath, '-f', 'gfm', '-t', 'html5', '--no-highlight'], { encoding: 'utf8' });

  const h2s = [...body.matchAll(/<h2[^>]*id="([^"]+)"[^>]*>([\s\S]*?)<\/h2>/g)]
    .map(m => ({ id: m[1], text: m[2].replace(/<[^>]+>/g, '').trim() }));
  const tocHtml = h2s.length > 1 ? `
<nav class="toc-float" aria-label="Quick contents">
  <h4>Contents</h4>
  <ol>
    ${h2s.map(h => `<li><a href="#${h.id}">${h.text}</a></li>`).join('\n    ')}
  </ol>
</nav>` : '';

  const L = lang(file);
  const dir = L === 'ar' ? 'rtl' : 'ltr';
  const title = file.replace(/\.md$/, '').replace(/[-_]/g, ' ');
  const tajawal = L === 'ar'
    ? `<link rel="preconnect" href="https://fonts.googleapis.com"><link href="https://fonts.googleapis.com/css2?family=Tajawal:wght@400;700&display=swap" rel="stylesheet">`
    : '';

  const html = `<!DOCTYPE html>
<html lang="${L}" dir="${dir}">
<head>
<meta charset="UTF-8">
<meta name="viewport" content="width=device-width, initial-scale=1">
<title>${title} — teachable-machine</title>
${tajawal}
<style>${CSS}</style>
</head>
<body dir="${dir}">
${siblingBar(file)}
${tocHtml}
<main>
${body}
</main>
</body>
</html>`;

  writeFileSync(htmlPath, html);
  return htmlPath;
}

const files = collect();
console.log(`📄 Building HTML for ${files.length} markdown file(s)\n`);

let failures = 0;
for (const md of files) {
  try {
    const html = render(md);
    console.log(`  ✓ ${relative(PKG, html)}`);
  } catch (err) {
    failures++;
    console.log(`  ⚠️  ${relative(PKG, md)}  —  ${err.message.split('\n')[0]}`);
  }
}

console.log(failures
  ? `\n⚠️  ${failures} failure(s) — is pandoc on PATH?`
  : `\n✅ All docs built.`);
